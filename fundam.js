let str = "Hello, World!"; // String
let num = 100;            // Number
let isActive = false;     // Boolean
let notDefined;           // Undefined
let empty = null;         // Null
let obj = { name: "Rahul", age: 25 }; // Object
let arr = [1, 2, 3, "four"];          // Array

console.log(typeof str); // Output: string
console.log(typeof num); // Output: number
console.log(typeof isActive); // Output: boolean
console.log(typeof notDefined); // Output: undefined
console.log(typeof empty); // Output: object
console.log(obj); // Output: { name: 'Rahul', age: 25 }
console.log(arr); // Output: [1, 2, 3, 'four']
