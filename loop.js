// for loop
for(let i = 1; i <= 5; i++) {
    console.log(i);  // 1, 2, 3, 4, 5
}

// while loop
let count = 1;
while(count <= 3) {
    console.log(count);  // 1, 2, 3
    count++;
}

// for-of loop (arrays ke liye)
let fruits = ["Apple", "Banana", "Mango"];
for(let fruit of fruits) {
    console.log(fruit);
}
