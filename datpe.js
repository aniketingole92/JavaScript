let x = 10, y = 5;

// Arithmetic operators
console.log(x + y); // 15 (Addition)
console.log(x - y); // 5 (Subtraction)
console.log(x * y); // 50 (Multiplication)
console.log(x / y); // 2 (Division)

// Comparison operators
console.log(x > y);  // true
console.log(x === y); // false (strict equality)

// Logical operators
console.log(true && false); // AND (false)
console.log(true || false); // OR (true)
console.log(!true);         // NOT (false)
