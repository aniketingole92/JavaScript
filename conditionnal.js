// If-else
let score = 85;
if (score >= 90) {
  console.log("Grade A");
} else if (score >= 80) {
  console.log("Grade B");
} else {
  console.log("Grade C");
}
// Output: Grade B

// For loop
for (let i = 0; i < 3; i++) {
  console.log("Iteration " + i);
}
// Output: Iteration 0, Iteration 1, Iteration 2
